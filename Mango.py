class Fruit :
    def Mango(self):
        print(f"name of the fruit is {self.name}")
        print(f"Colour of the fruit is {self.Colour}")
        print(f"Taste of the fruit is {self.Taste}")
        print(f"Season of the fruit is {self.Season}")
        print(f"Type of the fruit is {self.Type}")

Fruit1 = Fruit()
